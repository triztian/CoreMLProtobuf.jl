using ProtoBuf
import ProtoBuf.meta

mutable struct Identity <: ProtoType
    __protobuf_jl_internal_meta::ProtoMeta
    __protobuf_jl_internal_values::Dict{Symbol,Any}
    __protobuf_jl_internal_defaultset::Set{Symbol}

    function Identity(; kwargs...)
        obj = new(meta(Identity), Dict{Symbol,Any}(), Set{Symbol}())
        values = obj.__protobuf_jl_internal_values
        symdict = obj.__protobuf_jl_internal_meta.symdict
        for nv in kwargs
            fldname, fldval = nv
            fldtype = symdict[fldname].jtyp
            (fldname in keys(symdict)) || error(string(typeof(obj), " has no field with name ", fldname))
            if fldval !== nothing
                values[fldname] = isa(fldval, fldtype) ? fldval : convert(fldtype, fldval)
            end
        end
        obj
    end
end # mutable struct Identity
const __meta_Identity = Ref{ProtoMeta}()
function meta(::Type{Identity})
    ProtoBuf.metalock() do
        if !isassigned(__meta_Identity)
            __meta_Identity[] = target = ProtoMeta(Identity)
            allflds = Pair{Symbol,Union{Type,String}}[]
            meta(target, Identity, allflds, ProtoBuf.DEF_REQ, ProtoBuf.DEF_FNUM, ProtoBuf.DEF_VAL, ProtoBuf.DEF_PACK, ProtoBuf.DEF_WTYPES, ProtoBuf.DEF_ONEOFS, ProtoBuf.DEF_ONEOF_NAMES)
        end
        __meta_Identity[]
    end
end

export Identity
